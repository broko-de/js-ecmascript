/**
 * El método includes determina si un array o string incluye un determinado elemento. 
 * Devuelve true o false, si existe o no respectivamente.
 */
let charactes = ['Mario','Luigi','Toad','Peach','Warrio'];

console.log(charactes.includes('Mario'));
// hay que tener en cuenta es case sensitive, es decir que tiene en cuenta las minúsculas y mayúsculas
console.log(charactes.includes('toad'));